    private void btnrespuestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnrespuestaActionPerformed
        String respuesta;
        numero2=txtpantalla.getText();
        if(!numero2.equals("")){
            respuesta=calculadora(numero1,numero2,signo);
            txtpantalla.setText(respuesta);
        }
    }//GEN-LAST:event_btnrespuestaActionPerformed
public static String calculadora(String numero1,String numero2,String signo){
    Double res=0.0;
    String respuesta;
    if(signo.equals("+")){
        res=Double.parseDouble(numero1)+Double.parseDouble(numero2);
    }if(signo.equals("-")){
        res=Double.parseDouble(numero1)-Double.parseDouble(numero2);
    }if(signo.equals("*")){
        res=Double.parseDouble(numero1)*Double.parseDouble(numero2);
    }if(signo.equals("/")){
        res=Double.parseDouble(numero1)/Double.parseDouble(numero2);
    }
    respuesta=res.toString();
    return respuesta;
}
    
    
